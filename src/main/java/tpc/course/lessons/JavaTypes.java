package tpc.course.lessons;

public class JavaTypes{

	public static void main(String[] args){

		boolean bool0 = true;
		System.out.println("bool0: " + bool0);

		byte byte0 = 55;
		System.out.println("byte0: " + byte0);

		byte byte1 = -128;
		System.out.println("byte1: " + byte1);

		byte byte2 = 127;
		System.out.println("byte2: " + byte2);

		short short0 = 4679;
		System.out.println("short0: " + short0);

		int int0 = -33698;
		System.out.println("int0: " + int0);

		long long0 = 900_000_000_000_000L;
		System.out.println("long0: " + long0);

		float float0 = 55.789F;
		System.out.println("float0: " + float0);

		double double0 = 55.7897483904034785304D;
		System.out.println("double0: " + double0);

		char char0 = '*';
		System.out.println("char0: " + char0);

		char char1 = 'L';
		System.out.println("char1: " + char1);

		char char2 = '6';
		System.out.println("char2: " + char2);
	}
}
